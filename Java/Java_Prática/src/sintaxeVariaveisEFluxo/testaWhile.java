package sintaxeVariaveisEFluxo;

public class testaWhile {
	public static void main(String[] args) {
		        int contador = 0;  // escopo é importanteeee
		        while(contador <= 10) {
		            System.out.println(contador);
		            contador = contador + 1;
		        }
		        System.out.println(contador);		
	}
}
